import UIKit

extension UIColor {
    class func skyBlueColor() -> UIColor {
        return UIColor(red: 44/255.0, green: 126/255.0, blue: 226/255.0, alpha: 1)
    }
    
    class func halfSkyBlueColor() -> UIColor {
        return UIColor(red: 44/255.0, green: 126/255.0, blue: 226/255.0, alpha: 0.5)
    }
}